public class BasicCalculation {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        
        System.out.println("加算: " + (a + b));
        System.out.println("減算: " + (a - b));
        System.out.println("乗算: " + (a * b));
        System.out.println("除算: " + (a / b));
        System.out.println("剰余: " + (a % b));
    }
}
